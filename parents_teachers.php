				<h2>The Parent's Guide to Sexual Abuse</h2>
<p><strong>What is child sexual abuse?</strong></p>
<p>In the broadest definition, child sexual abuse is any forced or coerced sexual activity involving a child. Bribes or other tricks may be used. Often, the abuser persuades the child to cooperate and to keep the abuse a secret.</p>
<p><strong>If my child has been sexually abused, what can I do?</strong></p>
<p>Sexual abuse is upsetting and confusing for children and parents. <br>
  Being informed will help you make good decisions. <br>
  Talk with your child and let her or him be involved in decisions. <br>
  Your child needs to know that you are not afraid or upset and that you are listening to them. &nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp; </p>
<p> After the abuse, the behavior of your child may change for a time. You may notice any of the following:</p>
<ul>
  <li>Heightened fear or anxiety</li>
  <li>Increased tearfulness or crying</li>
  <li>Disturbed sleeping patterns</li>
  <li>Loss of appetite</li>
  <li>Irritable nature, losing patience easily</li>
  <li>Temper tantrums</li>
  <li>Isolation or secrecy</li>
  <li>Difficulty at school, such as poor concentration and loss of interest in classroom activities.</li>
</ul>
<p><strong>How should I respond? </strong></p>
<p>After learning your child has been sexually abused, try to respond with patience and kindness to your child, yourself, and your family. Try these suggestions:</p>
<ul>
  <li><strong>Help</strong> your child to express their feelings.</li>
</ul>
<ul>
  <li><strong>Do not blame</strong> the child for what has happened. Tell your child that you are not angry with her or&nbsp;him and that they did nothing wrong.</li>
</ul>
<ul>
  <li><strong>Let your child know</strong> that you      are concerned and want to help them.</li>
</ul>
<ul>
  <li><strong>Restore</strong> your child's sense of control.</li>
</ul>
<ul>
  <li><strong>Do not push</strong> your child      to talk about the experience. If the subject comes up, discuss it       honestly and openly.</li>
</ul>
<ul>
  <li><strong>Maintain routines</strong> and return      to your family's usual activities as soon as possible. Don't become too protective of       your child.</li>
</ul>
<ul>
  <li><strong>Pay attention</strong> to the      needs of other children in your household.</li>
</ul>
<ul>
  <li><strong>Discuss</strong> your feelings with someone you      trust. This is not a time for you to       be "tough." It is a good time to seek       support for yourself.</li>
</ul>
<p><strong>For ideas of where to get help for you and your child, visit the <a href="bodies.php#section17">Sexual Violence Resources</a> section of this site. </strong></p>
<p>Source: Illinois Coalition Against Sexual Assault (ICASA), <em>The Parent Pamphlet</em>. <a href="http://www.icasa.org/docs/brochures/parent%20pamphlet%202010.pdf">(http://www.icasa.org/docs/brochures/parent%20pamphlet%202010.pdf</a>)</p>

