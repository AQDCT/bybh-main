				<h2>Factors That Affect Adolescent Health</h2>
				<p>Most health risk factors among adolescents are interrelated. Youth who engage in 
				one unhealthy behavior, smoking or drinking for example, are more likely to develop 
				a cluster of unhealthy behaviors and habits. Likewise, many protective factors such 
				as good nutrition or trusted relationships with adults, are interrelated and increase 
				the likelihood for healthy adolescent development. Reducing risk behaviors and 
				increasing protective factors positively affects health and well-being and helps 
				strengthen our youth to make healthy decisions, withstand life stressors, and grow 
				to become healthy adults who contribute positively to our community.</p> 
