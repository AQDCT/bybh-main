				<h2>Youth-Friendly Health Care – What Young People Say is Important to Them</h2>
				<p>When adults visit a health care facility, they want to be treated with respect by the provider.  It's no different for young people. Teens and young adults have specific criteria that they feel are important when accessing health care.</p>
<p>Here are some characteristics of health care that contribute to a young person's positive experience:</p>
<h3>Accessibility:</h3>
  <ul>
    <li><strong>Location, location, location!</strong> You can't get care if you can't get there.</li>
    <li><strong>Affordability</strong>. Young people often have limited financial resources, so pricing is key. No one wants to be surprised when something is more expensive than they thought it would be.</li>
  </ul>

<h3>Staff attitude:</h3>
   <ul>
    <li><strong>R-E-S-P-E-C-T</strong> Young people want to feel like their medical provider is respectful and supportive of them</li>

    <li><strong>Just trust me.</strong> Young people want honest and trustworthy providers.  If you don't trust someone, you don't feel like you can be honest with them and discuss sensitive topics</li>
    <li><strong>Friends first.</strong> Friendliness goes beyond smiling and being polite.  Young people say it's nice to have a provider who is interested in the nonmedical aspects of their lives and "treats them like a friend". Patients also appreciate when a provider shares their own personal experiences with them, whether it has to do with the visit or not.</li>
    </ul>
 
 <h3>Communication:</h3>
  <ul>
    <li><strong>Listen up! </strong> Young people want a provider who is a good listener</li>
    <li><strong>Straight talk.</strong> Providers should be clear and use a direct style when communicating with patients, but be careful not to use a lecturing tone.</li>
    </ul>


<h3>Medical competency of the provider:</h3>
  <ul>
    <li><strong>Ouch!</strong> Avoiding and managing pain is really important to young people. They don't want to feel physically uncomfortable during an examination, while getting a shot or getting blood drawn. Be gentle, and explain when things might hurt!</li>
    </ul>
 
<h3>Guideline-driven care:</h3>
  <ul>
    <li><strong>Give me the whole picture.</strong> Young people want a regular assessment of their overall health, which will change based on life events and personal goals.</li>
    
    <li><strong>Keep it confidential.</strong> Sensitive topics like reproductive and mental health demand an understanding of confidentiality between patient and provider.</li>

    <li><strong>Independence. </strong> As young people transition into adulthood, they need a certain amount of guidance, but they also say that their autonomy, or independence is really important.</li>
    </ul>



<h3>Age appropriate environments:</h3>
  <ul>
    <li><strong>Be flexible. </strong>Young people appreciate flexibility in office hours and scheduling appointments to avoid school absenteeism</li>
    <li><strong>Give me space.</strong> Young people appreciate having a separate space for youth-focused care, including handouts specifically for them.</li>
    <li><strong>Don't make me wait. </strong>If you're kept waiting, it may feel like the provider doesn't respect you enough to make time for you. If I have to wait, keep me entertained with games, TV, and a clean, welcoming waiting room.</li>
    <li><strong>Who are you again? </strong>It's important that young people experience continuity, and that they see the same provider whenever they visit. This is important to establish trust.</li>
    <li><strong>Privacy, please. </strong>Young people value their privacy, and it's important that they feel their privacy respected during their office visit.</li>
  </ul>


<h3>Youth involvement in health care:</h3>
  <ul>
    <li><strong>Give me a say!</strong> Young people want to be involved in their care and feel like they understand their health and treatment.</li>
  </ul>
  <h3>Health outcomes:</h3>
  <ul>
    <li><strong>How are things turning out?</strong> After the visit is over, young people want the tools that help them live their lives free of pain, to be able to manage their emotional health, and control their reproductive health.</li>
  </ul>
</ul>


