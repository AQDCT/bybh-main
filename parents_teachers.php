				<h2>Talking About Sex</h2>
				<p>Talking to your child about sex and relationships can be difficult and you may 
				be tempted to put it off. But your child needs to learn about these subjects to 
				stay safe and grow to become a healthy, happy adult. As a parents, you are in 
				the best position to give your child the information they need and the advice 
				they want.</p>
				<p><b><i>Top Tips:</i></b></p>
				<ul style="list-style: disc; padding-left: 20px">
				<li>Be honest about your feelings.</li> 
				<li>It’s perfectly normal to feel uncomfortable, maybe even embarrassed, by 
				some of your children’s questions. You might find support and 
				encouragement from other adults if you share your experiences with 
				friends and other parents who know exactly where you are coming from.</li> 
				<li>If you don’t know the answer to a question, say so. But make a 
				commitment to find out and report back. Or better, find the answer 
				together.</li> 
				<li>When it’s appropriate, use humor. Laughing with your teen can help 
				takes the stress down a notch and help keep everyone feeling more 
				comfortable.</li> 
				<li>Think about your child’s particular questions. Is it curiosity or is there an 
				underlying anxiety that needs your attention?</li> 
				<li>When you talk together, find out what they already know and understand
				by asking gently probing questions like, ‘What do you think it means?’ 
				This gives you a good opportunity to correct wrong information if 
				necessary</li> 
				<li>Remember that the responses to questions received by young children 
				will lay the foundations for patterns of communication in later years. 
				Treat the questions seriously and make every effort to respond 
				effectively.</li> 
				<li>If it is helpful, use books, brochures or pictures to explain things, though 
				take care about the images you choose. One of our staff members 
				remembers her beloved mother drawing detailed, anatomical drawings 
				for her when she barely 7-years old. She laughs about it now but recalls 
				being a little scared and confused by the images.</li> 
				<li>Take care to finish your conversations by praising your child for asking 
				question and reminding them that you are always available and happy 
				for them to come to you with any future questions.</li> 
				<li>Remember that children need facts but they also need to be able to 
				share feelings and concerns and to seek guidance in making decisions. 
				Be the trusted adult they need to navigate this exciting, sometimes 
				confusing time.</li> 
				</ul>
