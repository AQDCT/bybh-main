				<h2>Adolescence</h2>
				<p>The transition from childhood to adulthood is one of the most dynamic in human development. The rate of physical, emotional, and intellectual change during adolescence is second only to infancy. As their bodies and brains shift through this extensive remodeling, adolescents transition from the care of others to become their own health and wellness managers.</p> 
				<p>That’s why it is essential to lay a strong foundation for adolescents so they can successfully move from home to the complicated world around them and see their future as healthy adults.</p> 
				<p>These years of development are when life-long patterns of healthy behavior are established. By recognizing, respecting, and protecting adolescence as a developmental stage, parents can foster safer, healthier and more responsible adolescents.</p> 
				<p>This period of development is the perfect time to help them develop positive adult relationships, provide reliable information, and develop strong links to the health care community.  It’s a balance. On one hand, you want to guide them around the pitfalls of adolescence; on the other you want to encourage their independence.</p> 
				<p>Each setting where young people live, learn, work, play and worship provides opportunities for youth to strengthen their attitudes toward their bodies and health.  Although studies show that adolescents learn primarily from their friends, they also want to learn from their parents and community leaders. This is good news; it means parents, teacher, and other trusted adults can nudge them away from hazards, direct them toward healthy choices, and help them navigate the developmental tasks of adolescence.</p> 
