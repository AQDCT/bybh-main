<h2>Protecting the Rights of Pregnant and Parenting Youth</h2>
 <h3> A Resource for Parents and Providers</h3>
  <p><strong><em>Is your child pregnant or about to become a parent?</em></strong><strong> </strong><br>
  Their rights are protected under Title IX, which is a federal law that prohibits discrimination based on sex in education programs or activities. All students have the same right to be involved in educational programs and extracurricular activities, including school clubs, academic societies, and honors programs. It also prohibits discrimination against pregnant and parenting students. (Note: if your child attends a school run by a religious organization, it is excluded from Title IX). </p>
<p><strong><em>Can a school require a pregnant student to participate in a separate program? </em></strong><br>
  No. A school may suggest an alternate program or school for pregnant students, but participation must be voluntary and up to the student. Pregnant students are allowed to stay in their regular classes and school if they choose. </p>
<p><strong><em>Can a school require a pregnant student to obtain a doctor's permission before allowing her to attend school?</em></strong><strong> </strong><br>
  No. Schools cannot require a pregnant student to turn in a doctor's note in order to stay in school or participate in activities, unless a doctor's note is required by all students being treated by a doctor (for example, a school can't ask for a doctor's note from a pregnant student unless they require it for all students who see a doctor, like a student with asthma). </p>
<p><strong><em>What types of assistance must a school provide to a pregnant student at school? </em></strong><strong> </strong><br>
  Schools must make adjustments to the regular program in order to accommodate a student's temporary pregnancy status, like providing a larger desk or allowing frequent trips to the restroom. </p>
<p><strong><em>Does a school have to excuse absences due to pregnancy or childbirth?</em></strong><strong> </strong><br>
  Yes. Under Title IX schools must excuse absences due to a student's pregnancy or related conditions for as long as the student's doctor thinks medically necessary. </p>
<p><strong><em>What if some teachers have their own policies about class attendance and make-up work? </em></strong><br>
  It's the school's responsibility to make sure that the policies of individual teachers do not discriminate against pregnant students. A pregnant student must be allowed to submit work after a deadline that she missed due to an absence relating to her pregnancy. If grading is based on class attendance or participation then the student should be allowed to earn the credits she missed. </p>
<p><strong><em>How do I file a complaint of discrimination?</em></strong><strong> </strong><br>
  If your child has been teased by classmates, left out of school programs, or not allowed to sign up for difficult classes due to a pregnancy, she can file a complaint of discrimination with the <u>Office of Civil Rights (OCR)</u>. [Text "Office of Civil Rights (OCR)" should be a link to <a href="http://www2.ed.gov/about/offices/list/ocr/complaintintro.html">http://www2.ed.gov/about/offices/list/ocr/complaintintro.html</a>] </p>
<p>For more details about rights under Title IX, and how to file a complaint and questions you may have about the law, read the report <u>Supporting the Academic Success of Pregnant and Parenting Students</u>. [hyperlink to the PDF<a href="http://www2.ed.gov/about/offices/list/ocr/docs/pregnancy.pdf">http://www2.ed.gov/about/offices/list/ocr/docs/pregnancy.pdf</a>]</p>

