<h2>Youth in Foster Care at Greater Risk for Teen Pregnancy</h2>

<p>While all adolescents are at risk for teenage pregnancy and parenthood, some groups face more challenges than others. Youth living in foster care experience particular barriers that lead to disparate outcomes.</p>
<p>One-third of girls in foster care become pregnant at least once by age 17, and nearly one-half of them do so by age 19. </p>
<p>[INSERT CHART]</p>
<p>More than half of boys living in foster care reported getting someone pregnant by age 21, compared to 19 percent of boys who were not living in foster care.       </p>
<p>[INSERT CHART]</p>
<h3>Why does this difference exist?</h3>
<p>Youth in care may face multiple risk factors:</p>
<ul>
  <li>Histories of abuse and neglect</li>
  <li>Lack of a consistent, trusting, and caring adult in their lives</li>
  <li>Lack of opportunity to experience normal teenage relationships</li>
  <li>Desire for someone to love</li>
</ul>
<p>Youth in care may also have faced homelessness and poverty, putting them further at risk.</p>
<p>Many of these risks are out of the youth's control and depend on the child welfare system, which often lacks funding for the delivery of evidence-based and youth in care-oriented sex education programs.</p>
<p>A young person transitioning out of foster care, particularly as a young parent, can be a destabilizing time.</p>
<h3>So what can I do?</h3>
<p>Build a relationship based on trust and compassion.</p>
<ul>
  <li>Some foster youth have had few positive relationships with adults and some may have experienced abuse and neglect. It is important to let them know early and often that they are welcome in your home and that you care about them. </li>
</ul>
<p>Talk with your foster children about sex, and be specific. </p>
<ul>
  <li>Talking to your foster child about sex is not a one-time conversation. It is an ongoing, two-way dialogue. When you start the conversation, be honest, open, non-judgmental, and respectful. You will be a better communicator if you are sensitive to your foster child's culture, religion, and sexual orientation. </li>
</ul>
<p>Spend quality time with your foster child.</p>
<ul>
  <li>Teens who are close to their parents/foster parents and feel supported by them are more likely to wait before having sex and use contraception more consistently. Family activities like going to the movies or cooking dinner together are good examples of spending quality time with each other and are perfect opportunities to have conversations. </li>
</ul>
<p>Supervise and monitor your foster children and adolescents.</p>
<ul>
  <li>Establish rules, curfews, and standards of expected behavior through open discussion. This may be difficult since some children will try to test your parental limits, but most will respect you. Supervising and monitoring your foster child's whereabouts doesn't make you a nag; it makes you a caring foster parent. </li>
</ul>
<p>Know your foster children's friends and their families.</p>
<ul>
  <li>Friends have a strong influence on each other, both positive and negative. Meet the parents of your foster child's friends and try to establish common rules and expectations. Some foster teens may not want anyone to know they are in foster care and will be reluctant for their foster parents to meet their friends. Don't be discouraged.</li>
</ul>
<p>Know what your foster kids are watching, reading, and listening to.</p>
<ul>
  <li>Today, teenagers spend over 40 hours each week consuming media. It is important to talk with your foster children about what the media portrays and what you think about it. Encourage them to think critically and ask how what they see on TV relates to their life. </li>
</ul>
<p>Don't forget the boys—Talk to your foster sons and foster daughters. Avoid the double standard.</p>
<ul>
  <li>Teen girls don't get pregnant on their own. Boys may feel pressurized to prove something to their friends or impress a girl. Talk with girls and boys about the emotional and health consequences of sex, responsibility, love, and values. </li>
</ul>
<p>Discourage dating at an early age. Watch out for age differences in relationships.</p>
<ul>
  <li>Take a stand against your daughter dating a boy significantly older than she and your son developing a relationship with a girl much younger than he. Set a limit of no more than two (at most three) year age difference.</li>
</ul>
<p>Encourage your foster child to become involved in positive activities.</p>
<ul>
  <li>Getting involved in activities like sports, arts, community-service, faith-based activities, or other after-school programs will help the youth build confidence and self-esteem by mastering skills. Provide them with alternatives to engaging in risky behavior.  </li>
</ul>
<p>Help your foster teens to have options for the future that are more attractive than early pregnancy and parenthood. </p>
<ul>
  <li>If your foster child believes they have a successful future ahead of them they are more likely to delay sex, pregnancy, and parenthood. Help them set meaningful goals for the future and talk to them in concrete terms about what it will take to reach their goals. Provide help along the way.</li>
</ul>
