			<h2>Facts and Myths</h2>
			<p class="p1"><strong></strong><strong><em>Bust these myths before you bust a nut&hellip;.&nbsp;</em></strong>&nbsp;</p>
			<p class="p3"><em>Condoms are the guy&rsquo;s responsibility a.k.a. carrying condoms makes you a slut.</em></p>
			<p class="p4">STIs and pregnancy are things that affect both people having sex&hellip; so why should one person have the responsibility for protecting both of you??&nbsp; Two people should talk about protection beforehand, and make a plan. Having condoms doesn&rsquo;t cause you to have sex&hellip; that&rsquo;s like saying carrying around a bottle of Tylenol causes you to get a headache.&nbsp; Having condoms handy at all times means you&rsquo;re more likely to use protection, it doesn&rsquo;t mean you&rsquo;re easy, or having sex with every person you happen to run into that day. Once you&rsquo;ve decided to become sexually active, carrying condoms is just smart. It means you&rsquo;ve considered the possible consequences of having sex and decided to protect yourself and your partner. &nbsp;</p>
			<p class="p3"><em>Condoms don&rsquo;t really protect against STIs and pregnancy</em>.</p>
			<p class="p3">No method is perfect, except abstaining from sexual activity altogether.&nbsp; But condoms, when used properly, drastically reduce the risk of getting an STI, or becoming pregnant when you don&rsquo;t want to be. There are some diseases that can still be spread, like those transmitted through skin-to-skin contact like genital warts, if the condom doesn&rsquo;t cover the affected area.</p>
			<p class="p3"><em>Wearing one condom is good.&nbsp; Wearing TWO condoms must be better.</em></p>
			<p class="p3"><em> </em>Math lets you down here. DON&rsquo;T double bag it. Wearing two condoms is less effective than one, because the friction between the two condoms can cause tears or holes, leaving you and your partner susceptible to STIs and pregnancy.</p>
			<p class="p3"><em>Stopping to put on a condom kills the mood.</em></p>
			<p class="p4">Condoms add an extra step, that&rsquo;s true.&nbsp; But seriously... you can&rsquo;t wait 20 more seconds??!&nbsp; This is another reason to keep condoms handy at all times once you&rsquo;ve decided to have sex. Also: make it a joint effort. Offer to help put the condom on. Practice putting one on yourself, or on your partner.</p>
			<p class="p3"><em>It&rsquo;s ok if we start having sex, then the guy puts on a condom before he finishes.</em></p>
			<p class="p3">Noooooooooo!! Not &lsquo;just for a second&rsquo;. Not &lsquo;just the tip&rsquo;. Not &lsquo;I promise I&rsquo;ll pull out&rsquo;. Before a guy ejaculates, he can release &lsquo;pre-ejaculate&rsquo;, which can mean transmission of STIs and/or pregnancy. <strong><em>Condom on, from the start of sexual contact, until the finish.</em></strong></p>
			<p class="p3"><em>Condoms make sex no fun&hellip; the guy can&rsquo;t feel anything.</em></p>
			<p class="p3">Condom companies spend a LOT of money developing products to make sure this isn&rsquo;t the case. Try different brands or styles of condoms, some are thinner than a human hair. Condoms make sex feel different: some say better, some say worse. If it&rsquo;s painful or uncomfortable, the guy may be using the condom <span class="s1"><strong>wrong</strong></span>, or could be using the <span class="s1"><strong>wrong size</strong></span>. Regular sized condoms can fit over your forearm or a 
size 10 shoe. However, maybe the ring feels too loose or too tight. If so, try out different sizes. Popular 
condom brands like Lifestyles, Durex and Trojan have smaller and larger sizes available. If you use a large .&nbsp; Some guys feel like they can last longer with condoms. Oh, and if a guy has been drinking or doing drugs, that can make him less sensitive, so maybe the condom isn&rsquo;t to blame!</p>
			<p class="p3"><em>Condoms can be reused.</em></p>
			<p class="p3"><em> </em>Do not do this.&nbsp; Once you open the wrapper, you have to use it or toss it.</p>
			<p class="p3"><em>No condom? No problem. I&rsquo;ll just use this plastic wrap. Or this plastic bag. Same thing, right?</em></p>
			<p class="p3">Don&rsquo;t use household items in place of condoms.&nbsp; Condoms are manufactured for a reason. They are tested for holes and designed specifically to fit a man&rsquo;s penis. Only condoms will provide the best protection against STIs and pregnancy.</p>
			<p class="p3"><span class="s1"><strong><em>The &lsquo;You Can&rsquo;t Get Pregnant If&hellip; &rsquo; </em></strong></span><em></em></p>
			<p class="p3">You can&rsquo;t get pregnant the first time you have sex. If the girl is on her period. If you have sex standing up. If the girl is on top. If the girl pees right after sex. If you&rsquo;re both virgins. If the girl takes a bath afterwards. If the girl jumps up and down afterwards. If the guy pulls out or doesn&rsquo;t go all the way in. If the girl sneezes afterwards. If the girl pushes on her belly button really hard afterwards.&nbsp; If the girl doesn&rsquo;t have an orgasm. If you&rsquo;re in a hot tub. If you&rsquo;re on the moon.&nbsp; If you&rsquo;re both wearing football helmets. If you do the Hokey Pokey right afterward&hellip; ok SERIOUSLY. None of these are true. Use a condom.</p>
			<p class="p3"><em>Even if math isn&rsquo;t you&rsquo;re favorite subject, you will get this equation.</em></p>
			<p class="p4"><em>Penis + Vagina = Baby</em></p>
			<p class="p4"><em>Penis+Condom+Vagina= No Baby</em></p>
