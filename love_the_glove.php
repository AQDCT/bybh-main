			<h2>Contraceptives</h2>
			<p>Condoms are great! Not only can they prevent pregnancy but they can also prevent
			STIs. When used perfectly, condoms have a 98% or 99% effectiveness rate. But
			people aren’t perfect. They make mistakes. You will, too. So what do you do? You
			find your “Plus One,” the birth control method that you use in addition to condoms. So
			what’s your Plus One? Take a look at the methods described here and talk with your
			health provider to find the best method for you.</p>
			<p>Many of you already know that abstinence (not having sex) is the only 100% way to
			avoid unplanned pregnancies and STIs. But abstinence isn’t for everyone. When the
			time comes and you’re ready to have sex, take some more time to prepare your plan
			for avoiding an unplanned pregnancy or an STI. While the following methods of birth control are not 100% effective, effectiveness rates increase when you follow the directions and use the birth control perfectly. The rates given are “typical use” rates, because people 
			make mistakes.</p>
			<!-- Image: TheNationalCampaign.org -->
		
			<!-- Image: TheNationalCampaign.org -->
			<p class="p1"><strong>An IUD (intrauterine device)</strong>- This is a small, &ldquo;T&rdquo; shaped device that is flexible and is inserted into the uterus by a medical professional. Mirena, ParaGard and Skyla are brands that are available. Mirena can be left in for up to 5 years, ParaGard up to 10 years and Skyla (which is the smallest) can be left in for up to 3 years. IUDs are 99% effective!&nbsp;</p>
			<!-- Image: TheNationalCampaign.org -->
			<p class="p1"><em>Learn more:</em></p>
			<ul>
			<li><a href="http://www.mirena-us.com/index.php%20" target="_blank">Mirena Website</a></li>
			<li><a href="http://www.paragard.com/%20" target="_blank">ParaGard</a></li>
			<li><a href="http://www.skyla-us.com/index.php" target="_blank">Skyla</a></li>
			</ul>
			<p class="p1"><strong>The Implant (Implanon) - </strong>Implanon is a small rod that is about the size of a matchstick and a medical professional inserts it into your arm. You can feel the rod under your skin but you can barely see it. Impanon lasts for up to 3 years and is 99% effective!&nbsp;</p>
			<!-- Image: TheNationalCampaign.org -->
			<p class="p1"><em>Learn more:</em></p>
			<ul>
			<li><a href="http://www.implanon-usa.com/en/consumer/index.asp" target="_blank">Implanon</a></li>
			</ul>
			<p class="p1"><strong>The Shot (Depo-Provera) - </strong>Also know an Depo. This is a shot that a medical professional must give to you. You will need to get a new shot every 3 months. Some say that Depo can decrease bone strength and density so it&rsquo;s important to talk to a healthcare provider about the shot. The shot is 94% effective!&nbsp;</p>
			<!-- Image: TheNationalCampaign.org -->
			<p class="p1"><em>Learn more:</em></p>
			<ul>
			<li><a href="http://www.plannedparenthood.org/health-topics/birth-control/birth-control-shot-depo-provera-4242.htm" target="_blank">Planned Parenthood info on Depo</a></li>
			<li><a href="http://www.mayoclinic.com/health/depo-provera/MY00995" target="_blank">Depo Information from Mayo Clinic</a></li>
			</ul>
			<p class="p1"><strong>Birth Control Pills - </strong>Sometimes these are called oral contraceptives. Pills must be taken at the same time every day. There are a ton of pill brands that have different side effects. Some brands have two types of hormons- progestin and estrogen while some are progestin-only. Most pills come in 21 or 28 day packs. There are also extended brands that come in 91 day packs. Talk to a healthcare provider to determine the best option for you. Pills are effective 91% of the time! You have to see a healthcare provider and receive a prescription to use this method.</p>
			<!-- Image: TheNationalCampaign.org -->
			<p class="p1"><em>Learn more:&nbsp;</em></p>
			<ul>
			<li><a href="http://www.loestrin24.com/loestrin/" target="_blank">Loestrin</a></li>
			<li><a href="http://www.myortho360.com/tricyclen/welcome" target="_blank">Ortho TriCyclen</a></li>
