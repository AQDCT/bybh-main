			<li><a href="http://www.plannedparenthood.org/health-topics/birth-control/birth-control-pill-4228.htm" target="_blank">Planned Parenthood Birth Control Pill information</a></li>
			</ul>
			<p class="p1"><strong>The Ring (NuvaRing) - </strong>The ring is about 2 inches across, thin and very flexible. The ring is a monthly birth control method that you yourself insert into your vagina. You leave the ring inside for 3 weeks and take out for your monthly period. However, you can keep the ring in all 4 weeks if you wish and then replace it. The ring is 91% effective! You have to see a healthcare provider and receive a prescription to use this method.</p>
			<!-- Image: TheNationalCampaign.org -->
			<p class="p1"><em>Learn more:</em></p>
			<ul>
			<li><a href="http://www.nuvaring.com/Consumer/index.asp" target="_blank">NuvaRing</a></li>
			</ul>
			<p class="p1"><strong>The Patch (Ortho Evra) - </strong>The patch is a thin piece of plastic that you stick onto your skin yourself. It is similar to a bandaid and is a weekly birth control method. Each week you wear a new patch for 3 weeks and then take off for your monthly period during the 4<sup>th</sup> week. You can choose where to stick the patch and it is 91% effective! You have to see a healthcare provider and receive a prescription to use this method.&nbsp;</p>
			<!-- Image: TheNationalCampaign.org -->
			<p class="p1"><em>Learn more:</em></p>
			<ul>
			<li><a href="http://www.orthoevra.com/index.html" target="_blank">Ortho Evra</a></li>
			</ul>
			<p class="p1"><strong>Diaphragm - </strong>The diaphragm is a dome-shaped cup that is shallow, made of silicone and flexible. You insert it inside the vagina yourself.&nbsp; You have to see a healthcare provider to get a diaphragm. It is 88% effective!</p>
			<!-- Image: TheNationalCampaign.org -->
			<ul>
			<li><a href="http://www.plannedparenthood.org/health-topics/birth-control/diaphragm-4244.htm" target="_blank">Planned Parenthood information on diaphragms</a></li>
			</ul>
			<p class="p1"><strong>Sponge - </strong>The sponge is a small, round piece of white plastic foam and you insert it into your vagina yourself. Sponges are available at health centers, clinics, drugstores and online. You are not required to see a health care provider to get the sponge. It is 76%- 88% effective!</p>
			<!-- Image: TheNationalCampaign.org -->
			<p class="p1"><em>Learn more:</em></p>
			<ul>
			<li><a href="http://www.plannedparenthood.org/health-topics/birth-control/birth-control-sponge-today-sponge-4224.htm" target="_blank">Planned Parenthood information on sponges</a></li>
			</ul>
			<p class="p1"><strong>Withdrawal Method - </strong>This is also known as &ldquo;pulling out&rdquo;. This is when a guy pulls his penis out right before he ejaculates or comes. This method will not protect against HIV and/or STIs. Pulling out is only 73- 78% effective. Some experts say that &ldquo;pre-cum&rdquo; or &ldquo;pre-ejaculate&rdquo; : (Which is the ejaculate or cum that 
comes out of the penis before orgasm) can still cause pregnancy. Also, if you spill any semen near the vaginal opening it can still lead to pregnancy.&nbsp;</p>
			<!-- Image: TheNationalCampaign.org -->
			<p class="p1"><em>Learn more:</em></p>
